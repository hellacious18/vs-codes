import java.util.*;

public class S3_13 
{
	public static void main(String[] args) 
	{
		byte B = 3;
		short S = 10;
		int I = 28;
		long L = 2001;
 		float F = 8.0F;
 		double D = 180.0D;
 		char C = 'N';
 		boolean B2 = false;
 
		Byte ByteObj = B;
 		Short ShortObj = S;
 		Integer IntObj = I;
 		Long LongObj = L;
 		Float FloatObj = F;
 		Double DoubleObj = D;
 		Character CharObj = C;
 		Boolean BoolObj = B2;
 		System.out.println("\n\tPRINTING OBJECT VALUES:\n");
 		System.out.println("\tByte object: " + ByteObj);
 		System.out.println("\tShort object: " + ShortObj);
 		System.out.println("\tInteger object: " + IntObj);
 		System.out.println("\tLong object: " + LongObj);
 		System.out.println("\tFloat object: " + FloatObj);
 		System.out.println("\tDouble object: " + DoubleObj);
 		System.out.println("\tCharacter object: " + CharObj);
 		System.out.println("\tBoolean object: " + BoolObj);
 	}
}